dojo.provide('pwl.widget.ToolbarTitlePane');

/******************************************************************************/
/******************************************************************************/

dojo.require('pwl.widget.TitlePane');

dojo.require('dijit.Toolbar');
dojo.require('dijit.form.Button');

dojo.require('pwl.widget.titlePane.ToolbarTitleWidget');

/******************************************************************************/

dojo.declare(
	'pwl.widget.ToolbarTitlePane',
	[pwl.widget.TitlePane],
{
	title_widget: 'pwl.widget.titlePane.ToolbarTitleWidget',

/******************************************************************************/
/** public **/
/******************************************************************************/

/******************************************************************************/
/** Startup, Teardown *********************************************************/

	startup: function ()
	{
		this.inherited(arguments);
		
		this.getChildren().forEach( function ( i_child )
		{
			if ( i_child.isInstanceOf(dijit.Toolbar) )
				this.w_title_widget.set('w_toolbar', i_child);
		}, this);
		
		this.w_title_widget.resize();
	},

/******************************************************************************/
/** Events ********************************************************************/

/******************************************************************************/
/** protected **/
/******************************************************************************/
	
/******************************************************************************/
/** Attr handlers *************************************************************/

	
});